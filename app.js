function bindViewActions() {
  const debtForm = $("#debtForm");
  if (debtForm) {
    debtForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const plan = planDefinitions.find((item) => item.id === state.plan);
      if (state.debts.length >= plan.debts) return showLimit(planLimitMessage("debts"));
      const form = new FormData(debtForm);
      const balance = Number(form.get("balance"));
      const debt = {
        name: form.get("name"),
        balance,
        rate: Number(form.get("rate")),
        minPayment: Number(form.get("minPayment"))
      };
      if (state.user) {
        const row = await dbInsert("debts", {
          name: debt.name, balance: debt.balance, rate: debt.rate, min_payment: debt.minPayment
        });
        if (row) debt.id = row.id;
      }
      state.debts.push(debt);
      renderView();
    });
  }

  const memberForm = $("#memberForm");
  if (memberForm) {
    memberForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const plan = planDefinitions.find((item) => item.id === state.plan);
      if (state.members.length >= plan.members) return showLimit(planLimitMessage("members"));
      const form = new FormData(memberForm);
      const member = { name: form.get("name"), role: form.get("role"), email: form.get("email") || "" };

      if (state.user) {
        // Invitation réelle: le backend crée le compte du membre et l'envoie par courriel
        if (!BACKEND_URL) {
          return showLimit(state.lang === "fr"
            ? "Le serveur d'invitations n'est pas joignable depuis cette adresse."
            : "The invitation server is not reachable from this address.");
        }
        const submitButton = memberForm.querySelector("button[type=submit]");
        submitButton.disabled = true;
        try {
          const response = await fetch(`${BACKEND_URL}/api/members/invite`, {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({
              inviterId: state.user.id,
              email: member.email,
              name: member.name,
              role: member.role
            })
          });
          const result = await response.json();
          if (response.ok && result.invited) {
            member.id = result.member.id;
            state.members.push(member);
            renderView();
            showLimit(result.linkedExisting
              ? (state.lang === "fr"
                ? `${member.email} avait déjà un compte: il a été rattaché à votre famille et à votre plan.`
                : `${member.email} already had an account: it was linked to your family and plan.`)
              : (state.lang === "fr"
                ? `Invitation envoyée à ${member.email}.`
                : `Invitation sent to ${member.email}.`));
          } else if (result.error === "already_in_family") {
            showLimit(state.lang === "fr"
              ? "Ce membre fait déjà partie de votre famille."
              : "This member is already part of your family.");
          } else if (result.error === "member_limit_reached") {
            showLimit(planLimitMessage("members"));
          } else if (result.error === "email_already_registered") {
            showLimit(state.lang === "fr"
              ? "Un compte existe déjà avec ce courriel."
              : "An account already exists with this email.");
          } else if (result.error === "email_rate_limited") {
            showLimit(state.lang === "fr"
              ? "Limite d'envoi de courriels atteinte (Supabase gratuit: ~2/heure). Réessayez dans une heure."
              : "Email sending limit reached (free Supabase: ~2/hour). Try again in an hour.");
          } else if (result.error === "email_invalid") {
            showLimit(state.lang === "fr"
              ? "Cette adresse courriel est refusée par Supabase."
              : "This email address is rejected by Supabase.");
          } else {
            showLimit(state.lang === "fr"
              ? "L'invitation a échoué. Vérifiez que le backend est démarré."
              : "The invitation failed. Check that the backend is running.");
          }
        } catch (_error) {
          showLimit(state.lang === "fr"
            ? "Backend injoignable: démarrez le serveur (port 3000) puis réessayez."
            : "Backend unreachable: start the server (port 3000) and try again.");
        } finally {
          submitButton.disabled = false;
        }
        return;
      }

      state.members.push(member);
      renderView();
    });
  }

  const transactionForm = $("#transactionForm");
  if (transactionForm) {
    transactionForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(transactionForm);
      const transaction = {
        date: form.get("date"),
        name: form.get("name"),
        category: form.get("category"),
        amount: Number(form.get("amount"))
      };
      if (state.user) {
        const row = await dbInsert("transactions", transaction);
        if (row) transaction.id = row.id;
      }
      state.transactions.unshift(transaction);
      renderView();
    });
  }

  const goalForm = $("#goalForm");
  if (goalForm) {
    goalForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(goalForm);
      const goal = {
        name: form.get("name"),
        target: Number(form.get("target")),
        saved: Number(form.get("saved") || 0)
      };
      if (state.user) {
        const row = await dbInsert("goals", goal);
        if (row) goal.id = row.id;
      }
      state.goals.push(goal);
      renderView();
    });
  }

  const budgetForm = $("#budgetForm");
  if (budgetForm) {
    budgetForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(budgetForm);
      const item = {
        category: form.get("category"),
        planned: Number(form.get("planned")),
        spent: Number(form.get("spent") || 0)
      };
      if (state.user) {
        const row = await dbInsert("budget_categories", item);
        if (row) item.id = row.id;
      }
      state.budget.push(item);
      renderView();
    });
  }

  const removeBindings = [
    ["data-remove-debt", "debts", "debts"],
    ["data-remove-member", "family_members", "members"],
    ["data-remove-transaction", "transactions", "transactions"],
    ["data-remove-goal", "goals", "goals"],
    ["data-remove-budget", "budget_categories", "budget"]
  ];
  removeBindings.forEach(([attr, table, key]) => {
    $$(`[${attr}]`).forEach((button) => button.addEventListener("click", async () => {
      const index = Number(button.getAttribute(attr));
      const item = state[key][index];
      if (state.user && item && item.id) await dbDelete(table, item.id);
      state[key].splice(index, 1);
      renderView();
    }));
  });

  const changePlanButton = $("#changePlanButton");
  if (changePlanButton) {
    changePlanButton.addEventListener("click", () => {
      history.replaceState(null, "", "#pricing");
      showLandingPage("pricing");
    });
  }

  const autoRenewToggle = $("#autoRenewToggle");
  if (autoRenewToggle) {
    autoRenewToggle.addEventListener("change", async (event) => {
      const autoRenew = event.target.checked;
      const note = $("#subNote");
      event.target.disabled = true;
      try {
        const response = await fetch(`${BACKEND_URL}/api/billing/auto-renew`, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ userId: state.user.id, autoRenew })
        });
        if (response.ok) {
          if (state.subscription) state.subscription.cancelAtPeriodEnd = !autoRenew;
          note.textContent = autoRenew
            ? (state.lang === "fr" ? "Renouvellement automatique activé." : "Auto-renew enabled.")
            : (state.lang === "fr" ? "Renouvellement désactivé: l'abonnement se terminera à l'échéance." : "Auto-renew disabled: subscription ends at expiry.");
          note.hidden = false;
          renderView();
        } else {
          throw new Error("failed");
        }
      } catch (_error) {
        event.target.checked = !autoRenew;
        note.textContent = state.lang === "fr"
          ? "Échec. Vérifiez que le backend est démarré."
          : "Failed. Check that the backend is running.";
        note.hidden = false;
      } finally {
        event.target.disabled = false;
      }
    });
  }

  const cancelSubButton = $("#cancelSubButton");
  if (cancelSubButton) {
    cancelSubButton.addEventListener("click", async () => {
      const confirmMsg = state.lang === "fr"
        ? "Résilier votre abonnement maintenant ? Vous repasserez au plan gratuit."
        : "Cancel your subscription now? You will return to the free plan.";
      if (!confirm(confirmMsg)) return;
      const note = $("#subNote");
      cancelSubButton.disabled = true;
      try {
        const response = await fetch(`${BACKEND_URL}/api/billing/cancel`, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ userId: state.user.id })
        });
        if (response.ok) {
          state.plan = "free";
          state.subscription = null;
          $("#activePlanLabel").textContent = "Free";
          updateUpgradeButton();
          renderPricing();
          renderView();
        } else {
          throw new Error("failed");
        }
      } catch (_error) {
        note.textContent = state.lang === "fr"
          ? "Échec de la résiliation. Vérifiez que le backend est démarré."
          : "Cancellation failed. Check that the backend is running.";
        note.hidden = false;
        cancelSubButton.disabled = false;
      }
    });
  }

  const passwordForm = $("#passwordForm");
  if (passwordForm) {
    passwordForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(passwordForm);
      const newPassword = form.get("newPassword");
      const note = $("#passwordNote");
      const { error } = await supabaseClient.auth.updateUser({ password: newPassword });
      note.textContent = error
        ? (state.lang === "fr" ? "Impossible de changer le mot de passe: " : "Could not change password: ") + error.message
        : (state.lang === "fr" ? "Mot de passe mis à jour." : "Password updated.");
      note.hidden = false;
      if (!error) passwordForm.reset();
    });
  }

  const settingsLang = $("#settingsLang");
  if (settingsLang) {
    settingsLang.value = state.lang;
    $("#settingsCurrency").value = state.currency;
    $("#settingsTheme").value = state.theme;
    settingsLang.addEventListener("change", (event) => updatePreference("lang", event.target.value));
    $("#settingsCurrency").addEventListener("change", (event) => updatePreference("currency", event.target.value));
    $("#settingsTheme").addEventListener("change", (event) => updatePreference("theme", event.target.value));
  }
}

// Message de limite adapté au plan réel de l'utilisateur
function planLimitMessage(kind) {
  const plan = planDefinitions.find((item) => item.id === state.plan);
  const fr = state.lang === "fr";
  if (kind === "debts") {
    if (!state.user) return t("demoLimitDebt");
    return fr
      ? `Limite du plan ${plan.name}: ${plan.debts} dettes maximum. Passez à un plan supérieur pour des dettes illimitées.`
      : `${plan.name} plan limit: ${plan.debts} debts maximum. Upgrade for unlimited debts.`;
  }
  if (!state.user) return t("demoLimitMember");
  const upgradeHint = plan.id === "family"
    ? (fr ? "Passez à Family Plus pour 10 membres." : "Upgrade to Family Plus for 10 members.")
    : (fr ? "Passez à Family (5 membres) ou Family Plus (10 membres)." : "Upgrade to Family (5 members) or Family Plus (10 members).");
  return fr
    ? `Limite du plan ${plan.name}: ${plan.members} membre${plan.members > 1 ? "s" : ""} maximum. ${upgradeHint}`
    : `${plan.name} plan limit: ${plan.members} member${plan.members > 1 ? "s" : ""} maximum. ${upgradeHint}`;
}

function showLimit(message) {
  const box = $("#limitMessage");
  if (!box) return;
  box.innerHTML = `<p class="pill-warn">${message}</p>`;
}

function updatePreference(key, value) {
  state[key] = value;
  persistPreferences();
  applyTheme();
  applyTranslations();
}

function openAuth(mode) {
  state.authMode = mode;
  const message = $("#authMessage");
  message.hidden = true;
  message.textContent = "";
  $("#authSwitch").textContent = mode === "register"
    ? (state.lang === "fr" ? "Déjà un compte ? Se connecter" : "Already have an account? Sign in")
    : (state.lang === "fr" ? "Pas de compte ? Créer un compte" : "No account? Create one");
  const forgot = $("#forgotPassword");
  forgot.hidden = mode === "register";
  forgot.textContent = state.lang === "fr" ? "Mot de passe oublié ?" : "Forgot password?";
  $(".topbar").classList.remove("menu-open");
  $("#authModeLabel").textContent = mode === "register" ? t("createAccount") : t("login");
  $("#authTitle").textContent = mode === "register"
    ? (state.lang === "fr" ? "Créer votre espace famille" : "Create your family workspace")
    : (state.lang === "fr" ? "Accéder à BudgetHub Family" : "Access BudgetHub Family");
  $("#authCopy").textContent = mode === "register"
    ? (state.lang === "fr"
      ? "Créez votre espace pour suivre budget, dettes, objectifs et membres de votre foyer."
      : "Create your workspace to track budget, debts, goals, and household members.")
    : (state.lang === "fr"
      ? "Connectez-vous pour retrouver votre budget familial, vos dettes et vos objectifs au même endroit."
      : "Sign in to keep your family budget, debts, and goals organized in one place.");
  const authForm = $("#authForm");
  authForm.reset();
  authForm.elements.password.setAttribute(
    "autocomplete",
    mode === "register" ? "new-password" : "current-password"
  );
  showView("auth");
}

function boot() {
  applyTheme();
  renderPricing();
  applyTranslations();
  loadExchangeRates();

  $("#languageSelect").addEventListener("change", (event) => updatePreference("lang", event.target.value));
  $("#currencySelect").addEventListener("change", (event) => updatePreference("currency", event.target.value));
  $("#themeToggle").addEventListener("click", () => updatePreference("theme", state.theme === "dark" ? "light" : "dark"));
  $("#menuToggle").addEventListener("click", () => $(".topbar").classList.toggle("menu-open"));
  $("#startDemo").addEventListener("click", openApp);
  $("#heroDemo").addEventListener("click", openApp);
  $("#backToLanding").addEventListener("click", () => showLandingPage("home"));
  $("#authBack").addEventListener("click", () => showLandingPage("home"));

  // Navigation multi-pages: Fonctions / Tarifs / Sécurité sont des pages distinctes
  $$(".landing-nav a").forEach((link) => {
    link.addEventListener("click", (event) => {
      event.preventDefault();
      const page = link.getAttribute("href").replace("#", "");
      history.replaceState(null, "", `#${page}`);
      showLandingPage(page);
    });
  });
  document.querySelector(".brand").addEventListener("click", (event) => {
    event.preventDefault();
    history.replaceState(null, "", "#home");
    showLandingPage("home");
  });
  const pricingLink = document.querySelector('a[href="#pricing"].secondary-link, .hero a[href="#pricing"]');
  if (pricingLink) {
    pricingLink.addEventListener("click", (event) => {
      event.preventDefault();
      history.replaceState(null, "", "#pricing");
      showLandingPage("pricing");
    });
  }
  $("#openLogin").addEventListener("click", () => openAuth("login"));
  $("#topRegister").addEventListener("click", () => openAuth("register"));
  $("#openRegister").addEventListener("click", () => openAuth("register"));
  $("#authSwitch").addEventListener("click", () => openAuth(state.authMode === "register" ? "login" : "register"));
  $("#logoutButton").addEventListener("click", async () => {
    if (supabaseClient) await supabaseClient.auth.signOut();
    setSessionUser(null);
    state.plan = "free";
    loadDemoData();
    showLandingPage("home");
  });

  $("#forgotPassword").addEventListener("click", async () => {
    const email = $("#authForm").elements.email.value.trim();
    const message = $("#authMessage");
    if (!email) {
      message.textContent = state.lang === "fr"
        ? "Entrez votre courriel ci-dessus, puis cliquez de nouveau."
        : "Enter your email above, then click again.";
      message.hidden = false;
      return;
    }
    const { error } = await supabaseClient.auth.resetPasswordForEmail(email, {
      redirectTo: window.location.origin + window.location.pathname
    });
    message.textContent = error
      ? (state.lang === "fr" ? "Impossible d'envoyer le courriel de réinitialisation." : "Could not send the reset email.")
      : (state.lang === "fr" ? "Courriel de réinitialisation envoyé. Vérifiez votre boîte." : "Reset email sent. Check your inbox.");
    message.hidden = false;
  });

  if (supabaseClient) {
    supabaseClient.auth.getSession().then(({ data }) => {
      if (data.session) {
        setSessionUser(data.session.user);
        loadProfilePlan();
        syncBillingPlan();
        loadUserData();
        openApp();
      }
    });

    supabaseClient.auth.onAuthStateChange((event, session) => {
      if (event === "PASSWORD_RECOVERY" && session) {
        // L'utilisateur arrive depuis le lien de réinitialisation: ouvrir les paramètres
        setSessionUser(session.user);
        state.currentView = "settings";
        openApp();
        renderView();
      }
      if (event === "SIGNED_IN" && session && !state.user) {
        // Connexion via lien de confirmation courriel
        setSessionUser(session.user);
        loadProfilePlan();
        syncBillingPlan();
        loadUserData();
        openApp();
      }
    });
  }
  $("#upgradeButton").addEventListener("click", () => {
    if (state.user && state.plan !== "free") {
      state.currentView = "account";
      renderView();
      return;
    }
    history.replaceState(null, "", "#pricing");
    showLandingPage("pricing");
  });
  $("#openWorkspace").addEventListener("click", () => {
    showView("app");
    renderView();
  });
  $("#userEmailChip").addEventListener("click", () => {
    state.currentView = "account";
    showView("app");
    renderView();
  });
  // Au retour d'un paiement Stripe (autre onglet), re-synchroniser le plan
  window.addEventListener("focus", () => {
    if (state.user) syncBillingPlan();
  });
  $("#authForm").addEventListener("submit", async (event) => {
    event.preventDefault();
    const form = event.target;
    const email = form.elements.email.value.trim();
    const password = form.elements.password.value;
    const message = $("#authMessage");
    const submitButton = form.querySelector("button[type=submit]");

    if (!supabaseClient) {
      message.textContent = state.lang === "fr"
        ? "Service d'authentification indisponible. Réessayez plus tard."
        : "Authentication service unavailable. Please try again later.";
      message.hidden = false;
      return;
    }

    submitButton.disabled = true;
    message.hidden = true;

    try {
      if (state.authMode === "register") {
        const { data, error } = await supabaseClient.auth.signUp({
          email,
          password,
          options: { emailRedirectTo: window.location.origin + window.location.pathname }
        });
        if (error) throw error;
        if (data.user && !data.session) {
          message.textContent = state.lang === "fr"
            ? "Compte créé. Vérifiez votre courriel pour confirmer votre adresse."
            : "Account created. Check your email to confirm your address.";
          message.hidden = false;
          return;
        }
      } else {
        const { data, error } = await supabaseClient.auth.signInWithPassword({ email, password });
        if (error) throw error;
        setSessionUser(data.user);
        loadProfilePlan();
        syncBillingPlan();
        loadUserData();
      }
      openApp();
    } catch (error) {
      const friendly = {
        "Invalid login credentials": state.lang === "fr" ? "Courriel ou mot de passe invalide." : "Invalid email or password.",
        "User already registered": state.lang === "fr" ? "Un compte existe déjà avec ce courriel." : "An account already exists with this email.",
        "Email not confirmed": state.lang === "fr" ? "Confirmez votre courriel avant de vous connecter." : "Please confirm your email before signing in."
      };
      message.textContent = friendly[error.message] || (state.lang === "fr"
        ? "Une erreur est survenue. Réessayez."
        : "Something went wrong. Please try again.");
      message.hidden = false;
    } finally {
      submitButton.disabled = false;
    }
  });
  $$("#appNav button").forEach((button) => button.addEventListener("click", () => {
    state.currentView = button.dataset.view;
    renderView();
  }));

  // Page d'atterrissage selon le hash (ex: #pricing), sans casser les liens de confirmation Supabase
  const initialHash = window.location.hash.replace("#", "");
  if (landingPages.includes(initialHash) && initialHash !== "home") {
    showLandingPage(initialHash);
  }
}

boot();
