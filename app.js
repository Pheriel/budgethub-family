function money(value) {
  const meta = currencyMeta[state.currency];
  return new Intl.NumberFormat(meta.locale, {
    style: "currency",
    currency: state.currency,
    maximumFractionDigits: value % 1 ? 2 : 0
  }).format(value * meta.rate);
}

