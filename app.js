function bindViewActions() {
  const debtForm = $("#debtForm");
  if (debtForm) {
    debtForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(debtForm);
      const payload = {
        name: form.get("name"),
        balance: Number(form.get("balance")),
        rate: Number(form.get("rate")),
        min_payment: Number(form.get("minPayment"))
      };
      if (state.editing.table === "debts") {
        const debt = findEditable(state.debts);
        if (canSyncUndatedMonthlyData() && debt.id) await dbUpdate("debts", debt.id, payload);
        Object.assign(debt, { name: payload.name, balance: payload.balance, rate: payload.rate, minPayment: payload.min_payment });
        saveMonthData();
        state.editing = { table: null, id: null };
        renderView();
        return;
      }
      const plan = planDefinitions.find((item) => item.id === state.plan);
      if (state.debts.length >= plan.debts) return showLimit(planLimitMessage("debts"));
      const debt = { name: payload.name, balance: payload.balance, rate: payload.rate, minPayment: payload.min_payment };
      if (canSyncUndatedMonthlyData()) {
        const row = await dbInsert("debts", payload);
        if (row) debt.id = row.id;
      }
      state.debts.push(debt);
      saveMonthData();
      renderView();
    });
  }

  const memberForm = $("#memberForm");
  if (memberForm) {
    memberForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const plan = planDefinitions.find((item) => item.id === state.plan);
      if (state.members.length >= plan.members) return showLimit(planLimitMessage("members"));
      const form = new FormData(memberForm);
      const member = { name: form.get("name"), role: form.get("role"), email: form.get("email") || "" };

      if (state.user) {
        // Invitation réelle: le backend crée le compte du membre et l'envoie par courriel
        if (!BACKEND_URL) {
          return showLimit(state.lang === "fr"
            ? "Le serveur d'invitations n'est pas joignable depuis cette adresse."
            : "The invitation server is not reachable from this address.");
        }
        const submitButton = memberForm.querySelector("button[type=submit]");
        submitButton.disabled = true;
        try {
          const response = await fetch(`${BACKEND_URL}/api/members/invite`, {
            method: "POST",
            headers: { "Content-Type": "application/json" },
            body: JSON.stringify({
              inviterId: state.user.id,
              email: member.email,
              name: member.name,
              role: member.role
            })
          });
          const result = await response.json();
          if (response.ok && result.invited) {
            member.id = result.member.id;
            state.members.push(member);
            renderView();
            showLimit(result.linkedExisting
              ? (state.lang === "fr"
                ? `${member.email} avait déjà un compte: il a été rattaché à votre famille et à votre plan.`
                : `${member.email} already had an account: it was linked to your family and plan.`)
              : (state.lang === "fr"
                ? `Invitation envoyée à ${member.email}.`
                : `Invitation sent to ${member.email}.`));
          } else if (result.error === "already_in_family") {
            showLimit(state.lang === "fr"
              ? "Ce membre fait déjà partie de votre famille."
              : "This member is already part of your family.");
          } else if (result.error === "member_limit_reached") {
            showLimit(planLimitMessage("members"));
          } else if (result.error === "email_already_registered") {
            showLimit(state.lang === "fr"
              ? "Un compte existe déjà avec ce courriel."
              : "An account already exists with this email.");
          } else if (result.error === "email_rate_limited") {
            showLimit(state.lang === "fr"
              ? "Limite d'envoi de courriels atteinte (Supabase gratuit: ~2/heure). Réessayez dans une heure."
              : "Email sending limit reached (free Supabase: ~2/hour). Try again in an hour.");
          } else if (result.error === "email_invalid") {
            showLimit(state.lang === "fr"
              ? "Cette adresse courriel est refusée par Supabase."
              : "This email address is rejected by Supabase.");
          } else {
            showLimit(state.lang === "fr"
              ? "L'invitation a échoué. Vérifiez que le backend est démarré."
              : "The invitation failed. Check that the backend is running.");
          }
        } catch (_error) {
          showLimit(state.lang === "fr"
            ? "Backend injoignable: démarrez le serveur (port 3000) puis réessayez."
            : "Backend unreachable: start the server (port 3000) and try again.");
        } finally {
          submitButton.disabled = false;
        }
        return;
      }

      state.members.push(member);
      renderView();
    });
  }

  const transactionForm = $("#transactionForm");
  if (transactionForm) {
    transactionForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(transactionForm);
      const payload = {
        date: form.get("date"),
        name: form.get("name"),
        category: form.get("category"),
        amount: Number(form.get("amount"))
      };
      if (!payload.date.startsWith(state.selectedMonth)) {
        return showLimit(state.lang === "fr"
          ? "La transaction doit rester dans le mois sélectionné."
          : "The transaction must stay inside the selected month.");
      }
      if (state.editing.table === "transactions") {
        const tr = findEditable(state.transactions);
        if (state.user && tr.id) await dbUpdate("transactions", tr.id, payload);
        Object.assign(tr, payload);
        saveMonthData();
        state.editing = { table: null, id: null };
        renderView();
        return;
      }
      const transaction = { ...payload };
      if (state.user) {
        const row = await dbInsert("transactions", payload);
        if (row) transaction.id = row.id;
      }
      state.transactions.unshift(transaction);
      saveMonthData();
      renderView();
    });
  }

  const goalForm = $("#goalForm");
  if (goalForm) {
    goalForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(goalForm);
      const payload = {
        name: form.get("name"),
        target: Number(form.get("target")),
        saved: Number(form.get("saved") || 0),
        monthly_contribution: Number(form.get("monthlyContribution") || 0)
      };
      if (state.editing.table === "goals") {
        const goal = findEditable(state.goals);
        if (canSyncUndatedMonthlyData() && goal.id) await dbUpdate("goals", goal.id, payload);
        Object.assign(goal, { name: payload.name, target: payload.target, saved: payload.saved, monthlyContribution: payload.monthly_contribution });
        saveMonthData();
        state.editing = { table: null, id: null };
        renderView();
        return;
      }
      const goal = { name: payload.name, target: payload.target, saved: payload.saved, monthlyContribution: payload.monthly_contribution, createdAt: new Date().toISOString() };
      if (canSyncUndatedMonthlyData()) {
        const row = await dbInsert("goals", payload);
        if (row) { goal.id = row.id; goal.createdAt = row.created_at; }
      }
      state.goals.push(goal);
      saveMonthData();
      renderView();
    });
  }

  const budgetForm = $("#budgetForm");
  if (budgetForm) {
    budgetForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(budgetForm);
      const payload = {
        category: form.get("category"),
        planned: Number(form.get("planned"))
      };
      if (state.editing.table === "budget_categories") {
        const b = findEditable(state.budget);
        if (canSyncUndatedMonthlyData() && b.id) await dbUpdate("budget_categories", b.id, payload);
        Object.assign(b, { ...payload, spent: 0 });
        saveMonthData();
        state.editing = { table: null, id: null };
        renderView();
        return;
      }
      const item = { ...payload, spent: 0 };
      if (canSyncUndatedMonthlyData()) {
        const row = await dbInsert("budget_categories", payload);
        if (row) item.id = row.id;
      }
      state.budget.push(item);
      saveMonthData();
      renderView();
    });
  }

  const incomeForm = $("#incomeForm");
  if (incomeForm) {
    incomeForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const value = Number(new FormData(incomeForm).get("income") || 0);
      state.income = value;
      if (state.user) await updateIncome(value);
      saveMonthData();
      renderView();
    });
  }

  // Boutons "Modifier": passent le formulaire en mode édition
  const editBindings = [
    ["data-edit-debt", "debts"],
    ["data-edit-budget", "budget_categories"],
    ["data-edit-goal", "goals"],
    ["data-edit-transaction", "transactions"]
  ];
  editBindings.forEach(([attr, table]) => {
    $$(`[${attr}]`).forEach((button) => button.addEventListener("click", () => {
      state.editing = { table, id: button.getAttribute(attr) };
      renderView();
      const panel = $("#viewContainer");
      if (panel) panel.scrollIntoView({ behavior: "smooth", block: "start" });
    }));
  });

  const cancelEdit = $("#cancelEdit");
  if (cancelEdit) {
    cancelEdit.addEventListener("click", () => {
      state.editing = { table: null, id: null };
      renderView();
    });
  }

  const removeBindings = [
    ["data-remove-debt", "debts", "debts"],
    ["data-remove-member", "family_members", "members"],
    ["data-remove-transaction", "transactions", "transactions"],
    ["data-remove-goal", "goals", "goals"],
    ["data-remove-budget", "budget_categories", "budget"]
  ];
  removeBindings.forEach(([attr, table, key]) => {
    $$(`[${attr}]`).forEach((button) => button.addEventListener("click", async () => {
      const index = Number(button.getAttribute(attr));
      const item = state[key][index];
      const syncAllowed = key === "transactions" || key === "members" || canSyncUndatedMonthlyData();
      if (syncAllowed && state.user && item && item.id) await dbDelete(table, item.id);
      state[key].splice(index, 1);
      if (["debts", "transactions", "goals", "budget"].includes(key)) saveMonthData();
      renderView();
    }));
  });

  const changePlanButton = $("#changePlanButton");
  if (changePlanButton) {
    changePlanButton.addEventListener("click", () => {
      history.replaceState(null, "", "#pricing");
      showLandingPage("pricing");
    });
  }

  const autoRenewToggle = $("#autoRenewToggle");
  if (autoRenewToggle) {
    autoRenewToggle.addEventListener("change", async (event) => {
      const autoRenew = event.target.checked;
      const note = $("#subNote");
      event.target.disabled = true;
      try {
        const response = await fetch(`${BACKEND_URL}/api/billing/auto-renew`, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ userId: state.user.id, autoRenew })
        });
        if (response.ok) {
          if (state.subscription) state.subscription.cancelAtPeriodEnd = !autoRenew;
          note.textContent = autoRenew
            ? (state.lang === "fr" ? "Renouvellement automatique activé." : "Auto-renew enabled.")
            : (state.lang === "fr" ? "Renouvellement désactivé: l'abonnement se terminera à l'échéance." : "Auto-renew disabled: subscription ends at expiry.");
          note.hidden = false;
          renderView();
        } else {
          throw new Error("failed");
        }
      } catch (_error) {
        event.target.checked = !autoRenew;
        note.textContent = state.lang === "fr"
          ? "Échec. Vérifiez que le backend est démarré."
          : "Failed. Check that the backend is running.";
        note.hidden = false;
      } finally {
        event.target.disabled = false;
      }
    });
  }

  const cancelSubButton = $("#cancelSubButton");
  if (cancelSubButton) {
    cancelSubButton.addEventListener("click", async () => {
      const confirmMsg = state.lang === "fr"
        ? "Résilier votre abonnement maintenant ? Vous repasserez au plan gratuit."
        : "Cancel your subscription now? You will return to the free plan.";
      if (!confirm(confirmMsg)) return;
      const note = $("#subNote");
      cancelSubButton.disabled = true;
      try {
        const response = await fetch(`${BACKEND_URL}/api/billing/cancel`, {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ userId: state.user.id })
        });
        if (response.ok) {
          state.plan = "free";
          state.subscription = null;
          $("#activePlanLabel").textContent = "Free";
          updateUpgradeButton();
          renderPricing();
          renderView();
        } else {
          throw new Error("failed");
        }
      } catch (_error) {
        note.textContent = state.lang === "fr"
          ? "Échec de la résiliation. Vérifiez que le backend est démarré."
          : "Cancellation failed. Check that the backend is running.";
        note.hidden = false;
        cancelSubButton.disabled = false;
      }
    });
  }

  const passwordForm = $("#passwordForm");
  if (passwordForm) {
    passwordForm.addEventListener("submit", async (event) => {
      event.preventDefault();
      const form = new FormData(passwordForm);
      const newPassword = form.get("newPassword");
      const note = $("#passwordNote");
      const { error } = await supabaseClient.auth.updateUser({ password: newPassword });
      note.textContent = error
        ? (state.lang === "fr" ? "Impossible de changer le mot de passe: " : "Could not change password: ") + error.message
        : (state.lang === "fr" ? "Mot de passe mis à jour." : "Password updated.");
      note.hidden = false;
      if (!error) passwordForm.reset();
    });
  }

  const settingsLang = $("#settingsLang");
  if (settingsLang) {
    settingsLang.value = state.lang;
    $("#settingsCurrency").value = state.currency;
    $("#settingsTheme").value = state.theme;
    settingsLang.addEventListener("change", (event) => updatePreference("lang", event.target.value));
    $("#settingsCurrency").addEventListener("change", (event) => updatePreference("currency", event.target.value));
    $("#settingsTheme").addEventListener("change", (event) => updatePreference("theme", event.target.value));
  }
}

// Message de limite adapté au plan réel de l'utilisateur
function planLimitMessage(kind) {
  const plan = planDefinitions.find((item) => item.id === state.plan);
  const fr = state.lang === "fr";
  if (kind === "debts") {
    if (!state.user) return t("demoLimitDebt");
    return fr
      ? `Limite du plan ${plan.name}: ${plan.debts} dettes maximum. Passez à un plan supérieur pour des dettes illimitées.`
      : `${plan.name} plan limit: ${plan.debts} debts maximum. Upgrade for unlimited debts.`;
  }
  if (!state.user) return t("demoLimitMember");
  const upgradeHint = plan.id === "family"
    ? (fr ? "Passez à Family Plus pour 10 membres." : "Upgrade to Family Plus for 10 members.")
    : (fr ? "Passez à Family (5 membres) ou Family Plus (10 membres)." : "Upgrade to Family (5 members) or Family Plus (10 members).");
  return fr
    ? `Limite du plan ${plan.name}: ${plan.members} membre${plan.members > 1 ? "s" : ""} maximum. ${upgradeHint}`
    : `${plan.name} plan limit: ${plan.members} member${plan.members > 1 ? "s" : ""} maximum. ${upgradeHint}`;
}

