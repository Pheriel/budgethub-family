// Demande au backend de vérifier les achats Stripe récents et de mettre à jour le plan
async function syncBillingPlan() {
  if (!BACKEND_URL || !state.user) return;
  try {
    const response = await fetch(`${BACKEND_URL}/api/billing/sync/${state.user.id}`);
    if (response.ok) {
      const result = await response.json();
      if (result.updated) loadProfilePlan();
    }
  } catch (_error) {
    // Backend hors ligne: le plan sera synchronisé via webhook ou à la prochaine connexion
  }
}

async function loadProfilePlan() {
  if (!supabaseClient || !state.user) return;
  const { data, error } = await supabaseClient
    .from("profiles")
    .select("plan,subscription_status,cancel_at_period_end,current_period_end,billing_duration")
    .eq("id", state.user.id)
    .single();
  if (!error && data && data.plan) {
    state.plan = data.plan;
    state.subscription = data.plan === "free" ? null : {
      status: data.subscription_status,
      cancelAtPeriodEnd: data.cancel_at_period_end,
      currentPeriodEnd: data.current_period_end,
      duration: data.billing_duration
    };
    $("#activePlanLabel").textContent = planDefinitions.find((plan) => plan.id === state.plan)?.name || data.plan;
    updateUpgradeButton();
    renderPricing();
    renderView();
  }
}

