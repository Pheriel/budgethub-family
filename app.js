// Vrai pendant le traitement d'un lien courriel pour neutraliser le handler SIGNED_IN
let handlingEmailLink = false;

async function handleAuthEmailLink() {
  if (!supabaseClient) return false;
  const params = new URLSearchParams(window.location.search);
  const hashParams = new URLSearchParams(window.location.hash.replace(/^#/, ""));
  const onConfirmPage = window.location.pathname.startsWith("/auth/confirm");
  handlingEmailLink = onConfirmPage || Boolean(params.get("token_hash"))
    || Boolean(hashParams.get("access_token")) || Boolean(hashParams.get("error"))
    || Boolean(hashParams.get("error_description"));
  const confirmedMessage = state.lang === "fr"
    ? "Email confirmé, vous pouvez vous connecter."
    : "Email confirmed, you can sign in.";

  // Erreur renvoyée par Supabase après vérification du lien (expiré, déjà utilisé...)
  const linkError = hashParams.get("error_description") || params.get("error_description")
    || hashParams.get("error") || params.get("error");
  if (linkError) {
    history.replaceState(null, "", "/");
    showLoginMessage(state.lang === "fr"
      ? `Ce lien n'est plus valide (${linkError}). Demandez un nouveau courriel et réessayez.`
      : `This link is no longer valid (${linkError}). Request a new email and try again.`);
    return true;
  }

  // Lien avec token_hash + type: vérifier le jeton côté client
  const tokenHash = params.get("token_hash");
  const type = params.get("type");
  if (tokenHash && type) {
    const { data, error } = await supabaseClient.auth.verifyOtp({
      token_hash: tokenHash,
      type
    });

    if (error) {
      history.replaceState(null, "", "/");
      showLoginMessage(state.lang === "fr"
        ? "Ce lien n'est plus valide. Demandez un nouveau courriel et réessayez."
        : "This link is no longer valid. Request a new email and try again.");
      return true;
    }

    if (type === "recovery") {
      // L'utilisateur doit rester connecté pour choisir un nouveau mot de passe
      history.replaceState(null, "", "/");
      if (data.user) await openRecoverySession(data.user);
      return true;
    }

    if (type === "invite") {
      // Membre invité: il reste connecté et crée son mot de passe
      history.replaceState(null, "", "/");
      setSessionUser(data.user || null);
      openAuth("setPassword");
      return true;
    }

    // Courriel confirmé: retour à l'écran de connexion avec message de succès
    await supabaseClient.auth.signOut();
    history.replaceState(null, "", "/?confirmed=true");
    showLoginMessage(confirmedMessage, true);
    return true;
  }

  // Lien {{ .ConfirmationURL }}: Supabase a déjà vérifié et renvoie la session dans le hash
  if (hashParams.get("access_token") || hashParams.get("refresh_token")) {
    const hashType = hashParams.get("type");
    const { data } = await supabaseClient.auth.getSession();
    history.replaceState(null, "", "/");

    if (data.session && hashType === "recovery") {
      await openRecoverySession(data.session.user);
      return true;
    }

    if (data.session && hashType === "invite") {
      // Lien {{ .ConfirmationURL }} d'invitation: session ouverte, on demande le mot de passe
      setSessionUser(data.session.user);
      openAuth("setPassword");
      return true;
    }

    if (data.session) {
      await supabaseClient.auth.signOut();
    }
    history.replaceState(null, "", "/?confirmed=true");
    showLoginMessage(confirmedMessage, true);
    return true;
  }

  // Rechargement de /?confirmed=true sans session: réafficher le message
  if (params.get("confirmed") === "true") {
    history.replaceState(null, "", "/");
    const { data } = await supabaseClient.auth.getSession();
    if (!data.session) {
      showLoginMessage(confirmedMessage, true);
      return true;
    }
    return false;
  }

  // /auth/confirm sans paramètres: ne pas rester sur cette URL
  if (onConfirmPage) {
    history.replaceState(null, "", "/");
    showLoginMessage(state.lang === "fr"
      ? "Lien de confirmation incomplet. Ouvrez le lien reçu par courriel ou connectez-vous."
      : "Incomplete confirmation link. Open the link from your email or sign in.");
    return true;
  }

  return false;
}

function boot() {
  const wantsAdmin = window.location.pathname === "/admin";
  applyTheme();
  renderPricing();
  applyTranslations();
  loadExchangeRates();

  $$(".language-select").forEach((select) => {
    select.addEventListener("change", (event) => updatePreference("lang", event.target.value));
  });
  $$(".currency-select").forEach((select) => {
    select.addEventListener("change", (event) => updatePreference("currency", event.target.value));
  });
  $("#themeToggle").addEventListener("click", () => updatePreference("theme", state.theme === "dark" ? "light" : "dark"));
  $("#appThemeToggle").addEventListener("click", () => updatePreference("theme", state.theme === "dark" ? "light" : "dark"));
  $("#appMonthSelect").addEventListener("change", (event) => changeSelectedMonth(event.target.value));
  const setMobileMenu = (open) => {
    $(".topbar").classList.toggle("menu-open", open);
    $("#menuToggle").setAttribute("aria-expanded", String(open));
  };
  $("#menuToggle").addEventListener("click", () => {
    setMobileMenu(!$(".topbar").classList.contains("menu-open"));
  });
  // Fermer le menu mobile après un choix (lien ou bouton d'action, pas les préférences)
  $("#topbarMenu").addEventListener("click", (event) => {
    if (event.target.closest("a, #openWorkspace, #openLogin, #topRegister, #startDemo, #openSupport")) {
      setMobileMenu(false);
    }
  });
  // Fermer le menu mobile au clic à l'extérieur de la barre
  document.addEventListener("click", (event) => {
    const topbar = $(".topbar");
    if (topbar.classList.contains("menu-open") && !topbar.contains(event.target)) {
      setMobileMenu(false);
    }
  });
  $("#startDemo").addEventListener("click", openApp);
  $("#heroDemo").addEventListener("click", openApp);
  $("#heroRegister").addEventListener("click", () => openAuth("register"));
  $("#openSupport").addEventListener("click", openSupportModal);
  $("#appSupportButton").addEventListener("click", () => {
    closeAppMenu();
    openSupportModal();
  });
  const mobileSupportButton = $("#mobileSupportButton");
  if (mobileSupportButton) mobileSupportButton.addEventListener("click", () => {
    closeAppMenu();
    openSupportModal();
  });
  const appMenuToggle = $("#appMenuToggle");
  if (appMenuToggle) appMenuToggle.addEventListener("click", (event) => {
    event.stopPropagation();
    setAppMenu(!document.body.classList.contains("app-menu-open"));
  });
  const appMenuClose = $("#appMenuClose");
  if (appMenuClose) appMenuClose.addEventListener("click", closeAppMenu);
  const appMenuBackdrop = $("#appMenuBackdrop");
  if (appMenuBackdrop) appMenuBackdrop.addEventListener("click", closeAppMenu);
  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") closeAppMenu();
  });
  $("#supportClose").addEventListener("click", () => { $("#supportModal").hidden = true; });

  // Jauge de force en direct pendant la saisie du mot de passe
  $("#authForm").elements.password.addEventListener("input", (event) => {
    if (state.authMode === "register" || state.authMode === "setPassword") renderPasswordStrength(event.target.value);
  });

  // Boutons œil: afficher/masquer le mot de passe
  $$("[data-toggle-password]").forEach((button) => {
    button.addEventListener("click", () => {
      const input = button.parentElement.querySelector("input");
      const show = input.type === "password";
      input.type = show ? "text" : "password";
      button.textContent = show ? "🙈" : "👁";
    });
  });

  // Fermeture de la modale de confirmation courriel
  $("#emailModalClose").addEventListener("click", () => {
    $("#emailModal").hidden = true;
    openAuth("login");
  });
  $("#backToLanding").addEventListener("click", () => showLandingPage("home"));
  $("#authBack").addEventListener("click", () => showLandingPage("home"));

  // Navigation multi-pages: Fonctions / Tarifs / Sécurité sont des pages distinctes
  $$(".landing-nav a").forEach((link) => {
    link.addEventListener("click", (event) => {
      if (link.dataset.legalLink) return;
      event.preventDefault();
      const page = link.getAttribute("href").replace("#", "");
      history.replaceState(null, "", `#${page}`);
      showLandingPage(page);
    });
  });
  bindLegalLinks();
  document.querySelector(".brand").addEventListener("click", (event) => {
    event.preventDefault();
    history.replaceState(null, "", "#home");
    showLandingPage("home");
  });
  const pricingLink = document.querySelector('a[href="#pricing"].secondary-link, .hero a[href="#pricing"]');
  if (pricingLink) {
    pricingLink.addEventListener("click", (event) => {
      event.preventDefault();
      history.replaceState(null, "", "#pricing");
      showLandingPage("pricing");
    });
  }
  $("#openLogin").addEventListener("click", () => openAuth("login"));
  $("#topRegister").addEventListener("click", () => openAuth("register"));
  $("#openRegister").addEventListener("click", () => openAuth("register"));
  $("#authSwitch").addEventListener("click", () => openAuth(state.authMode === "register" ? "login" : "register"));
  $("#logoutButton").addEventListener("click", async () => {
    if (supabaseClient) await supabaseClient.auth.signOut();
    setSessionUser(null);
    state.plan = "free";
    loadMonthData(state.selectedMonth === currentMonthKey() ? demoData : emptyMonthData());
    showLandingPage("home");
  });

  $("#forgotPassword").addEventListener("click", async () => {
    const email = $("#authForm").elements.email.value.trim();
    const message = $("#authMessage");
    if (!email) {
      message.textContent = state.lang === "fr"
        ? "Entrez votre courriel ci-dessus, puis cliquez de nouveau."
        : "Enter your email above, then click again.";
      message.hidden = false;
      return;
    }
    const { error } = await supabaseClient.auth.resetPasswordForEmail(email, {
      redirectTo: authRedirectUrl
    });
    message.textContent = error
      ? (state.lang === "fr" ? "Impossible d'envoyer le courriel de réinitialisation." : "Could not send the reset email.")
      : (state.lang === "fr" ? "Courriel de réinitialisation envoyé. Vérifiez votre boîte." : "Reset email sent. Check your inbox.");
    message.hidden = false;
  });

  if (supabaseClient) {
    handleAuthEmailLink().then((handled) => {
      if (handled) return;
      return supabaseClient.auth.getSession().then(({ data }) => {
      if (data.session) {
        setSessionUser(data.session.user);
        if (wantsAdmin) state.currentView = "admin";
        loadProfilePlan();
        syncBillingPlan();
        loadUserData();
        openApp();
        handleCheckoutReturn();
      }
      });
    });

    supabaseClient.auth.onAuthStateChange((event, session) => {
      if (event === "PASSWORD_RECOVERY" && session) {
        // L'utilisateur arrive depuis le lien de réinitialisation: ouvrir les paramètres
        setSessionUser(session.user);
        state.currentView = "settings";
        openApp();
        renderView();
      }
      if (event === "SIGNED_IN" && session && !state.user && !handlingEmailLink) {
        // Connexion via lien de confirmation courriel
        setSessionUser(session.user);
        if (wantsAdmin) state.currentView = "admin";
        loadProfilePlan();
        syncBillingPlan();
        loadUserData();
        openApp();
      }
    });
  }
  $("#upgradeButton").addEventListener("click", handleUpgradeAction);
  const mobileUpgradeButton = $("#mobileUpgradeButton");
  if (mobileUpgradeButton) mobileUpgradeButton.addEventListener("click", handleUpgradeAction);
  $("#openWorkspace").addEventListener("click", () => {
    showView("app");
    renderView();
  });
  $("#userEmailChip").addEventListener("click", () => {
    state.currentView = "account";
    showView("app");
    renderView();
    refreshViewData("account");
  });
  // Au retour d'un paiement Stripe (autre onglet), re-synchroniser le plan
  window.addEventListener("focus", () => {
    if (state.user) syncBillingPlan();
  });
  $("#authForm").addEventListener("submit", async (event) => {
    event.preventDefault();
    const form = event.target;
    const email = form.elements.email.value.trim();
    const password = form.elements.password.value;
    const message = $("#authMessage");
    const submitButton = form.querySelector("button[type=submit]");

    if (!supabaseClient) {
      message.textContent = state.lang === "fr"
        ? "Service d'authentification indisponible. Réessayez plus tard."
        : "Authentication service unavailable. Please try again later.";
      message.hidden = false;
      return;
    }

    // Validations spécifiques à l'inscription et à la création de mot de passe
    if (state.authMode === "register" || state.authMode === "setPassword") {
      const confirmPassword = form.elements.confirmPassword.value;
      if (!passwordIsStrong(password)) {
        message.textContent = state.lang === "fr"
          ? "Le mot de passe ne respecte pas tous les critères de sécurité."
          : "The password does not meet all security criteria.";
        message.hidden = false;
        return;
      }
      if (password !== confirmPassword) {
        message.textContent = state.lang === "fr"
          ? "Les deux mots de passe ne sont pas identiques."
          : "The two passwords do not match.";
        message.hidden = false;
        return;
      }
    }

    submitButton.disabled = true;
    message.hidden = true;
    message.classList.remove("success");

    try {
      if (state.authMode === "setPassword") {
        // Membre invité: définit son mot de passe sur la session ouverte par le lien
        const { error } = await supabaseClient.auth.updateUser({ password });
        if (error) throw error;
        const { data } = await supabaseClient.auth.getSession();
        if (data.session) {
          setSessionUser(data.session.user);
          if (wantsAdmin) state.currentView = "admin";
          await loadProfilePlan();
          syncBillingPlan();
          await loadUserData();
          openApp();
          return;
        }
        // Pas de session (cas limite): retour connexion avec message clair
        showLoginMessage(state.lang === "fr"
          ? "Mot de passe créé. Connectez-vous pour rejoindre votre famille."
          : "Password created. Sign in to join your family.", true);
        return;
      }
      if (state.authMode === "register") {
        const { data, error } = await supabaseClient.auth.signUp({
          email,
          password,
          options: {
            emailRedirectTo: authRedirectUrl,
            data: {
              lang: state.lang,
              language: state.lang,
              locale: state.lang === "fr" ? "fr-CA" : "en"
            }
          }
        });
        if (error) throw error;
        if (data.user && !data.session) {
          showEmailModal(email);
          return;
        }
      } else {
        const { data, error } = await supabaseClient.auth.signInWithPassword({ email, password });
        if (error) throw error;
        setSessionUser(data.user);
        if (wantsAdmin) state.currentView = "admin";
        loadProfilePlan();
        syncBillingPlan();
        loadUserData();
      }
      openApp();
    } catch (error) {
      const friendly = {
        "Invalid login credentials": state.lang === "fr" ? "Courriel ou mot de passe invalide." : "Invalid email or password.",
        "User already registered": state.lang === "fr" ? "Un compte existe déjà avec ce courriel." : "An account already exists with this email.",
        "Email not confirmed": state.lang === "fr" ? "Confirmez votre courriel avant de vous connecter." : "Please confirm your email before signing in."
      };
      message.textContent = friendly[error.message] || (state.lang === "fr"
        ? "Une erreur est survenue. Réessayez."
        : "Something went wrong. Please try again.");
      message.hidden = false;
    } finally {
      submitButton.disabled = false;
    }
  });
  $$("#appNav button").forEach((button) => button.addEventListener("click", () => {
    navigateAppView(button.dataset.view);
  }));

  // Rafraîchit l'épargne des objectifs en temps réel (croissance par cotisation)
  if (!window.__goalTicker) {
    window.__goalTicker = setInterval(() => {
      // Ne jamais re-render pendant que l'utilisateur saisit dans un champ (sinon la saisie est effacée)
      const active = document.activeElement;
      const typing = active && (active.tagName === "INPUT" || active.tagName === "SELECT" || active.tagName === "TEXTAREA");
      if (!typing && !$("#appView").hidden && (state.currentView === "goals" || state.currentView === "dashboard")
        && state.goals.some((g) => g.monthlyContribution > 0) && !state.editing.table) {
        renderView();
      }
    }, 3000);
  }

  // Page d'atterrissage selon le hash (ex: #pricing), sans casser les liens de confirmation Supabase
  window.addEventListener("popstate", () => {
    const page = window.location.pathname.replace("/", "");
    if (page === "admin") {
      state.currentView = "admin";
      if (state.user) openApp();
      else openAuth("login");
    } else if (legalPages[page]) showLandingPage(page);
    else showLandingPage(window.location.hash.replace("#", "") || "home");
  });
  const pathPage = window.location.pathname.replace("/", "");
  if (pathPage === "admin") {
    if (!state.user) openAuth("login");
    else {
      state.currentView = "admin";
      openApp();
    }
    return;
  }
  if (legalPages[pathPage]) {
    showLandingPage(pathPage);
    return;
  }
  const initialHash = window.location.hash.replace("#", "");
  if (landingPages.includes(initialHash) && initialHash !== "home") {
    showLandingPage(initialHash);
  }
}

